@dataclass
class I18n:
    language: str = "ja"

    def set_language(self, language: str) -> None:
        self.language = language if language in TRANSLATIONS else "ja"

    def t(self, key: str, **kwargs: object) -> str:
        table = TRANSLATIONS.get(self.language, TRANSLATIONS["ja"])
        fallback = TRANSLATIONS["en"].get(key, key)
        template = table.get(key, fallback)
        if kwargs:
            return template.format(**kwargs)
        return template

